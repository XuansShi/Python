while True:
    if num == 1:
        print("您当前话费余额为：","元")
    elif num == 2:
        print("您当前剩余流量为：","G")
    elif num == 3:
        print("您当前剩余通话时长为：","min")
    elif num == 0:
        print("已退出自助查询系统")
        break
    else:
        print("输入错误，请重新输入")
    num = eval(input("请输入："))


